def get_touch_pos(city_id):
    """
    获取点击城池后弹窗内的攻击按钮坐标。
    有 touch 字段则返回 touch 坐标，否则返回默认 (666, 431)。
    """
    info = CITIES.get(city_id)
    if not info:
        return (666, 431)
    if len(info) > 4:
        return tuple(info[4])
    return (666, 431)


def is_reachable_from(target_id, my_city_ids):
    """
    根据 NEIGHBORS 图判断目标城池是否可从己方领土通畅到达。
    使用 BFS：从己方所有城池出发，能遍历到的城池即为可达。
    只有与己方领土相邻且可达的敌方城池才可攻击。
    """
    if target_id in my_city_ids:
        return True
    from collections import deque
    queue = deque(my_city_ids)
    visited = set(my_city_ids)
    while queue:
        cur = queue.popleft()
        for nb in NEIGHBORS.get(cur, []):
            if nb in visited:
                continue
            visited.add(nb)
            if nb == target_id:
                return True
            queue.append(nb)
    return False


def get_border_targets_from_kingdoms(kingdom_map, my_kingdom='wei'):
    """
    根据实时的国家占领情况（从世界地图模板匹配得到的 kingdom_map）获取可攻击的交界敌方城池。
    kingdom_map: {city_id: 'wei'|'wu'|'shu'} 由颜色/模板识别
    只按颜色取：与己方交界且为敌方的城池。其它过滤（可达性等）忽略。
    """
    my_ids = {cid for cid, k in kingdom_map.items() if k == my_kingdom}
    targets = []
    seen = set()
    for cid in my_ids:
        for nb in NEIGHBORS.get(cid, []):
            if nb in my_ids or nb in seen:
                continue
            nb_kingdom = kingdom_map.get(nb)
            if nb_kingdom is None or nb_kingdom == my_kingdom:
                continue
            seen.add(nb)
            info = CITIES.get(nb, (0, 0, '', ''))
            targets.append({
                'id': nb,
                'pos': (info[0], info[1]),
                'kingdom': nb_kingdom,
                'name': info[3] if len(info) > 3 else '',
                'from_ally': cid,
                'ally_pos': (CITIES[cid][0], CITIES[cid][1])
            })
    return targets


def get_border_targets(my_kingdom='wei'):
    """
    [已废弃，仅作兼容] 基于 CITIES 静态 kingdom 的旧实现。
    实际应使用 get_border_targets_from_kingdoms(kingdom_map, my_kingdom)。
    """
    my_ids = {cid for cid, v in CITIES.items() if v[2] == my_kingdom}
    targets = []
    seen = set()
    for cid in my_ids:
        for nb in NEIGHBORS.get(cid, []):
            if nb not in my_ids and nb not in seen:
                seen.add(nb)
                info = CITIES[nb]
                targets.append({'id': nb, 'pos': (info[0], info[1]),
                    'kingdom': info[2], 'name': info[3],
                    'from_ally': cid,
                    'ally_pos': (CITIES[cid][0], CITIES[cid][1])})
    return targets
